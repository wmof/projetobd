    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente cliente = new Cliente();
        cliente.setCodigo(jTextCodigo.getText());
        cliente.setNome(jTextNome.getText());
        cliente.setEndereco(jTextEndereco.getText());
        cliente.setTelefone(jTextTelefone.getText());
        BdCliente banco = new BdCliente();
        String cpf;
        cpf = jTextCpf.getText();
        try {
            banco.insertFisica(cliente, cpf);
        } catch (SQLException ex) {
            Logger.getLogger(AddCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(AddCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

